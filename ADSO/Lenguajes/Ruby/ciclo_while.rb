=begin
- CICLO WHILE -
  La estructura WHILE es una estructura repetitiva que se ejecuta en forma de bucle, es decir una y otra vez, mientras se cumpla con una condición.
  A diferencia de la estructura repetitiva FOR que se ejecuta con un número definido de veces, la estructura WHILE no tiene un número definido de repeticiones. Es decir, que se va a repetir mientras se cumpla una condición. Con esto hay que tener cuidado para NO caer en un bucle repetitivo infinito.
=end

# SU ESTRUCTURA ES:

 while true #Condicional o true
   puts "Hola Mundo"
 end
